import turtle

b = turtle.Turtle()

b.pencolor("red")
b.hideturtle()

b.left(90)
b.forward(200)
b.backward(100)
b.right(90)
b.forward(35)
b.circle(50,180)
b.forward(35)
b.left(90)
b.forward(200)
b.left(90)
b.forward(35)
b.circle(50,180)
b.forward(35)

b.left(90)
b.forward(100)

turtle.done()
